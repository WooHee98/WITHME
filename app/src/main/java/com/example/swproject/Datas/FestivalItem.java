package com.example.swproject.Datas;

public class FestivalItem {

    String f_id;
    String f_name;
    String f_date1;
    String f_date2;
    String f_area;
    String f_image;
    String f_tel;


    public FestivalItem() {
        this.f_id = "None";
        this.f_name = "None";
        this.f_date1 = "None";
        this.f_date2 = "None";
        this.f_area = "None";
        this.f_image = "None";
        this.f_tel = "None";
    }


    public String getF_tel() {
        return f_tel;
    }

    public void setF_tel(String f_tel) {
        this.f_tel = f_tel;
    }

    public String getF_date1() {
        return f_date1;
    }

    public void setF_date1(String f_date1) {
        this.f_date1 = f_date1;
    }

    public String getF_date2() {
        return f_date2;
    }

    public void setF_date2(String f_date2) {
        this.f_date2 = f_date2;
    }

    public String getF_image() {
        return f_image;
    }

    public void setF_image(String f_image) {
        this.f_image = f_image;
    }

    public String getF_id() {
        return f_id;
    }

    public void setF_id(String f_id) {
        this.f_id = f_id;
    }

    public String getF_name() {
        return f_name;
    }

    public void setF_name(String f_name) {
        this.f_name = f_name;
    }

    public String getF_area() {
        return f_area;
    }

    public void setF_area(String f_area) {
        this.f_area = f_area;
    }
}
